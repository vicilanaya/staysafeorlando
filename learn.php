					<p>Human trafficking, one of the most pervasive crimes in the world, is a modern form of human slavery. The victims of human trafficking are exploited through force, fraud, and coercion. In the United States, many individuals become human trtafficking victims after being smuggled into the country. However, most human trafficking victims in the country are U.S. citizens.</p>
					
					<p>There are two forms of human trafficking, sex trafficking and labor trafficking, as defined by U.S. Federal Code &#40;22 U.S.C. &#167; 7102&#40;9&#41;&#41;.</p>
						<ul>
							<li><strong>Sex Trafficking&#58; </strong>&#34;a commercial sex act is induced by force, fraud, or coercion, or in which the person induced to perform such act has not attained 18 years of age&#34;</li><br>
							<li><strong>Labor Trafficking&#58; </strong>&#34;the recruitment, harboring, transportation, provision, or obtaining of a person for labor or services, through the use of force, fraud, or coercion for the purpose of subjection to involuntary servitude, peonage, debt bondage, or slavery&#34;</li>
						</ul>
