				<section id="section2">
					<p>People of any age, nationality, gender, sex, race, socioeconomic status, and education level can fall victims of trafficking. The chance of becoming a victim increases in vulnerable populations. These <a href="https://www.ovcttac.gov/taskforceguide/eguide/1-understanding-human-trafficking/" target="_blank">at-risk individuals include</a> &#34;undocumented workers, runaway and homeless youth, individuals with substance abuse or addiction issues, and low income individuals&#34;</p>
					
					<p>As diverse as the state of Florida is, <a href="https://humantraffickinghotline.org/states" target="_blank">Florida is 3rd</a> in the country in reported cases of human trafficking. This crime is highly profitable and poses a low risk to traffickers. Unlike arms trafficking or drug trafficking, human traffickers can sell a person or their services over and over again through the life of the victim.</p>
					
					<p>Traffickers are as diverse as their victims. Sharing traits with their victims, such as cultural background or sex, helps traffickers understand the vulnerabilities and gain the trust of their victims. This circumstance becomes even more difficult when the trafficker is an acquaintance or family member.</p>
					
